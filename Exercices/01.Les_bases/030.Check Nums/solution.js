const num1 = 10;
const num2 = 20;

if (num2 > num1) {
    console.log("num2 est supérieur à num1");
} else if (num1 === num2) {
    console.log("num1 et num2 sont équivalents");
} else {
    console.log("num2 est inférieur à num1");
}
///////////////////////////////////////////////

// Ce code est une structure de contrôle conditionnelle 
// utilisant l'instruction if-else. Elle évalue si la 
// valeur stockée dans la variable "num2" est supérieure, 
// égale ou inférieure à la valeur stockée dans la variable "num1".

// Si "num2" est supérieur à "num1", la première condition 
// est vraie et le message "num2 est supérieur à num1" est 
// affiché dans la console.

// Si "num2" est égal à "num1", la deuxième condition est vraie 
// et le message "num1 et num2 sont équivalents" est affiché.

// Enfin, si "num2" est inférieur à "num1", la troisième condition 
// est vraie et le message "num2 est inférieur à num1" est affiché.

// En résumé, ce code permet de comparer deux nombres et d'afficher 
// un message en fonction de leur relation.


