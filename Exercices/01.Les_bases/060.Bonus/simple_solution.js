const num = 5;
let total = 0;

for (let i = 0; i < num; i++) {
    total = total + i
}
console.log(total);
///////////////////////////////////////////////

// Tout d'abord, une variable num est déclarée et initialisée 
// à la valeur de 5.

// Ensuite, une variable total est déclarée et initialisée 
// à la valeur de 0.

// Une boucle for est ensuite utilisée pour incrémenter une 
// variable i de 0 à la valeur de num, qui est 4 dans ce cas. 
// La boucle s'arrête lorsque i est égal à num.

// À chaque itération de la boucle, la valeur de i est ajoutée 
// à la valeur actuelle de total et stockée dans la variable total. 
// Cela signifie que, à chaque itération, la variable total est 
// mise à jour avec la somme de tous les nombres de 0 à i.

// Une fois que la boucle est terminée, la valeur finale de total 
// est imprimée dans la console à l'aide de console.log().

// Dans ce cas, la sortie attendue serait : 10, car total serait 
// égal à 0+1+2+3+4=10.

